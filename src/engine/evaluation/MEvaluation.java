    @Override
    public int getPositionValue(NaiveMailboxBoard board) {
        List<Integer> whitePieces = board.getWhitePieces();
        List<Integer> blackPieces = board.getBlackPieces();
        int materialScore = 0;
        for (int whitePiece : whitePieces) {
            materialScore += PIECE_VALUES_MIDGAME[whitePiece - 1];
        }
        for (int blackPiece : blackPieces) {
            materialScore += -PIECE_VALUES_MIDGAME[Math.abs(blackPiece) - 1];
        }

        int boardPositionScore = 0;
        int[] pieces = board.getPieces();
        for (int i = 0; i < 64; i++) {
            if (pieces[i] != 0) {
                int pieceIndex = Math.abs(pieces[i]) - 1;
                boardPositionScore += (pieces[i] > 0) ?
                        MIDGAME_PST[pieceIndex][i] :
                        -MIDGAME_PST[pieceIndex][63 - i];
            }
        }

        return materialScore + boardPositionScore;
    }

